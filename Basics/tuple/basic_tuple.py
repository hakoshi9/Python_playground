"""タプルの基礎"""
#タプルは複数の値をもてる変数
#通常の変数は値を一つしか持てない
#しかし、タプルは1つ以上の値を保持することができる
#リストとは扱いや使えるメソッドが違うので、用途によってリストと使い分ける必要がある

#タプルの記法1
tuple_one = 10, 20, 30
print('tuple_one:', tuple_one)
#簡単な記法だが、タプルの宣言とはわかりづらいかもしれない

#タプルの記法2
tuple_two = (30, 10, 50)
print('tuple_two:', tuple_two)
#()を追記する必要はあるが、こちらの方がより明示的なタプルの宣言である
